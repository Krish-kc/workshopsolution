    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $stars_rated = $request->input('workshop_rating');
         $workshop_id = $request->input('workshop_id');

         $exist_rating = Rating::where('user_id',Auth::id())->where('workshop_id',$workshop_id)->first();

         if($exist_rating){

            $exist_rating->stars_rated = $stars_rated;
            $exist_rating->update();

         }
         else{

             Rating::create([
                'user_id'=> Auth::id(),
                'workshop_id' => $workshop_id,
                'stars_rated' => $stars_rated,
             ]);
         }


         toastr()->success('Thank you for rating this workshop !');
         return redirect()->back();


    }

